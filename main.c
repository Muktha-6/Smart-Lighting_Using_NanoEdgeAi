void fill_light_buffer() {
	for (int i = 0; i < DATA_INPUT_USER; i++) {
		light = BH1750_ReadLux(&hi2c1);
		light_buffer[AXIS_NUMBER * i] = light;
		HAL_Delay(120);
	}
}

void inference() {
	uint16_t id_class_t0, id_class_tn;
	fill_light_buffer();

	neai_classification(light_buffer, output_class_buffer, &id_class_t0);
	for (int i = 0; i < CONFIRMATIONS_NB - 1; i++) {
		fill_light_buffer();
		neai_classification(light_buffer, output_class_buffer, &id_class_tn);
		if (id_class_t0 != id_class_tn) {
			break;
		}
	}

	if (id_class_t0 == id_class_tn) {
		printf("Detected Class is:");
		printf(id2class[id_class_t0]);
		printf("\r\n");
		if (strcmp(id2class[id_class_t0], "BRIGHT_LIGHT") == 0) {
			__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3, 25);
		}

		else if (strcmp(id2class[id_class_t0], "DARK_LIGHT") == 0) {
			__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3, 255);
		} else {
			__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3, 127);
		}
	} else {
		printf("?");
		printf("\r\n");
	}
}


